#Operadores binários, ou seja, necessitam de dois algarismos

n1 = float(input('Digite um número: '))
n2= float(input('Digite outro número: '))

#Adição > '+'
adicao = n1 + n2
print(adicao)
#Subtração > '-'
subtracao = n1 - n2
print(subtracao)
#Divisão > '/'
divisao = n1 / n2
print(divisao)
#Multiplicação '*'
multiplicacao = n1 * n2
print(multiplicacao)
#Potência '**'
potencia = n1 ** n2
print(potencia)
#Divisão inteira '//'
divisao_inteira = n1 // n2
print(divisao_inteira)
#Resto da Divisão '%'
resto = n1 % n2
print(resto)

